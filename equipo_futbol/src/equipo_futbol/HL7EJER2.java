    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
      String equipo;
      equipo= txtequipo.getText();
      int indice;
      indice =cbgrupo.getSelectedIndex();
      if(indice==1)molista1.addElement(equipo);
      if(indice==2)molista2.addElement(equipo);
      if(indice==3)molista3.addElement(equipo);
      txtequipo.setText("");
      cbgrupo.setSelectedIndex(0);
    }//GEN-LAST:event_jButton1ActionPerformed

